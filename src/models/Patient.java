package models;

public class Patient {

	private String email;
	private String serial;
	private String original_fpath;
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getSerial() {
		return serial;
	}
	public void setSerial(String serial) {
		this.serial = serial;
	}
	public String getOriginal_fpath() {
		return original_fpath;
	}
	public void setOriginal_fpath(String original_fpath) {
		this.original_fpath = original_fpath;
	}
		
}
